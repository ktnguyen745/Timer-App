package timer;

public class Task {

	// Instance Variables
	private Duration duration;
	private String name;
	
	
	// Getters and Setters
	public Duration getDuration() {
		return duration;
	}
	public void setDuration(Duration duration) {
		this.duration = duration;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
